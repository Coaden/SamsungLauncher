    public ArrayList<Token> get(String input) {
        if (TextUtils.isEmpty(input)) {
            return null;
        }
        ArrayList<Token> tokens = new ArrayList();
        int inputLength = input.length();
        tokens.add(getToken(input.charAt(0)));
        for (int i = 1; i < inputLength; i++) {
            Token token = getToken(input.charAt(i));
            switch (token.type) {
                case 1:
                case 3:
                    tokens.add(token);
                    break;
                case 2:
                    tokens.add(token);
                    break;
                default:
                    break;
            }
        }
        return tokens;
    }

    private Token getToken(char character) {
        Token token = new Token();
        String letter = Character.toString(character);
        token.source = letter;
        token.type = 2;
        if (character < 'Ā') {
            token.type = 1;
            token.target = letter;
        } else if (character < '一' || character > '龤') {
            token.type = 3;
            token.target = letter;
        } else {
            int code;
            switch (findMapper(character)) {
                case 1:
                    code = find(Map1.HANZI_TO_BPMF_MAP_1, (int) character);
                    break;
                case 2:
                    code = find(Map2.HANZI_TO_BPMF_MAP_2, (int) character);
                    break;
                case 3:
                    code = find(Map3.HANZI_TO_BPMF_MAP_3, (int) character);
                    break;
                case 4:
                    code = find(Map4.HANZI_TO_BPMF_MAP_4, (int) character);
                    break;
                case 5:
                    code = find(Map5.HANZI_TO_BPMF_MAP_5, (int) character);
                    break;
                case 6:
                    code = find(Map6.HANZI_TO_BPMF_MAP_6, (int) character);
                    break;
                case 7:
                    code = find(Map7.HANZI_TO_BPMF_MAP_7, (int) character);
                    break;
                case 8:
                    code = find(Map8.HANZI_TO_BPMF_MAP_8, (int) character);
                    break;
                case 9:
                    code = find(Map9.HANZI_TO_BPMF_MAP_9, (int) character);
                    break;
                case 10:
                    code = find(Map10.HANZI_TO_BPMF_MAP_10, (int) character);
                    break;
                default:
                    token.type = 3;
                    token.target = letter;
                    break;
            }
            if (code == -1) {
                token.type = 3;
                token.target = letter;
            } else {
                token.target = getBPMFString(code);
            }
        }
        return token;
    }

    private String getBPMFString(int code) {
        StringBuilder bpmf = new StringBuilder();
        int[] array = find(UNICODES, code);
        for (int i : array) {
            bpmf.append((char) i);
        }
        return bpmf.toString();
    }

    private static int findMapper(int c) {
        int i = 0;
        while (i < devider.length) {
            if (c >= devider[i] && c < devider[i + 1]) {
                return i + 1;
            }
            i++;
        }
        return -1;
    }

    private static int find(int[][] values, int key) {
        int lowerBound = 0;
        int upperBound = values.length - 1;
        while (true) {
            int curIn = (lowerBound + upperBound) / 2;
            if (values[curIn][0] == key) {
                return values[curIn][1];
            }
            if (lowerBound > upperBound) {
                return -1;
            }
            if (values[curIn][0] < key) {
                lowerBound = curIn + 1;
            } else {
                upperBound = curIn - 1;
            }
        }
    }

